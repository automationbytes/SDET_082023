package Day1;

public class ComparisonOperator {
    public static void main(String[] args) {
        int a = 10;
        int b = 15;
        //equal
        System.out.println(a == b);

        //not equal
        System.out.println(a != b);

        //lesser
        System.out.println(a < b);

        System.out.println(a > b);

        System.out.println(a <= b);

        System.out.println(a >= b);
        
        
    }
}
