package Day1;

public class UnaryOperator {
    public static void main(String[] args) {
        int a  = 5;
       // ++a;
        System.out.println(a++); //5
        System.out.println(a); //+6
        //assigning + incrementing

        int b = 5;
        System.out.println(++b); //incrementing -> 6
        System.out.println(b);//assigning
    }
}
